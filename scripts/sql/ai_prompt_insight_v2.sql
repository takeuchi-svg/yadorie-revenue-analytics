from ai_prompt where prompt_key in ('summary', 'issue');

-- サマリ
update ai_prompt set
  content = $ak${month}の当施設の月次実績を、灯（あなた）が支配人に届ける「読みやすいレポート」として作成してください。だらだらした長文にせず、見出し・箇条書き・表で要点をつかめる形にします。

【構成】
1. 冒頭に1〜2文のねぎらい（灯の語り口。例「支配人、○月おつかれさまでした。」）。
2. 「### 今月の要点」— 主要指標をMarkdownの表で。行は 売上／稼働率／客単価／同伴係数／予算達成率。各行に「前年同月比」または「予算比」を併記。
3. 「### 売上」「### 予実（利益）」「### 生産性」の3セクション。各2〜4個の箇条書きで、良かった点・気になる点を簡潔に。
   - 予実: 営業損益の予算差・前年差と、原価・販管費など費用面の所感。施設タイプの基準PL（目標水準）が渡っていれば、それに照らして一言。
   - 生産性: 売上高人件費率・従業員1人1時間あたり売上・総労働時間/総残業時間。勤怠データが無い月はその旨を1行だけ。
4. 「### 次の一手」— 来月に向けた前向きな提案を1〜3個の箇条書きで。
5. 任意で、売上か稼働率の推移をchartコードブロックで1つ添えてよい。

【ルール】灯の語り口（誠実・裁かない・悪い数字も次の一手とセット）で、ただし各項目は簡潔に。数値は¥・%・カンマ付きで正確に。与えられた【実データ】のみを根拠にし推測しない。個人単位の給与・人件費には触れない。内部のテーブル名・列名・英語IDは本文に出さない。$ak$,
  status = 'published', updated_by = 'system:K40fix', updated_at = now()
where prompt_key = 'summary';

-- 課題と対策
update ai_prompt set
  content = $ak${month}を起点に当施設のデータを分析し、改善余地のある「課題」と「対策」を、支配人がスキャンして掴める形で抽出してください。

【頭の中で行う（手順の説明文は出力しない）】直近の推移・前年同月比・季節性・変化点(急に悪化/改善した月)を踏まえて課題を特定し、各課題に具体策を立て、代替案・実現可能性・副作用(他KPIへの悪影響)まで自己検証して最良案を選ぶ。

【出力】課題を重要な順に3〜4点。各課題は次の形で簡潔に（長い散文にしない）:
#### 1. 〈課題の見出し（15字以内）〉
- **現状**: 根拠となる数値・推移を1〜2行
- **対策**: 具体的な打ち手を1〜2行（なぜ最適かを一言）
- _自己検証_: 検討した代替案と、それを採らなかった理由を1行

【ルール】灯の語り口（詰めない・裁かない・「〜してみませんか」と前を向ける言葉。語りには温度を、数値は正確に）。参考情報のため断定を避け仮説として提示。他施設に触れる場合は施設タイプ・単価帯の違いを明記。個人給与・内部の英語ID/列名は本文に出さない。与えられた【実データ】のみを根拠にし推測しない。前置き・手順の説明文は書かず、課題のみを示す。$ak$,
  status = 'published', updated_by = 'system:K40fix', updated_at = now()
where prompt_key = 'issue';

